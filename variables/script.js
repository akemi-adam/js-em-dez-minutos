
// Variável de escopo global
var primeiraVariavel = "Hello Variáveis!"

console.log(primeiraVariavel)

// Variável de escopo local
let segundaVariavel = 12

console.log(segundaVariavel)

// Variável constante
const terceiraVariavel = 09.6

console.log(terceiraVariavel)

// Variável de escopo global armazenando um valor booleano (verdadeiro ou falso)
var quartaVariavel = true;

console.log(quartaVariavel) 

// Utilizando o operador typeof para saber o tipo de uma variável

console.log(`Tipo da primeira da variável: ${typeof primeiraVariavel}`)

console.log('Tipo da segunda variável: ' + typeof segundaVariavel)

console.log('Tipo da quarta variável: ' + typeof quartaVariavel)


 
// Operações númericas

console.log(segundaVariavel / 2); // A '/' pode ser substituída por + (soma), - (subtração) ou * (multiplicação)

// Concatenação (soma de duas strings)

console.log('Isso é uma concatenação ' + primeiraVariavel)